#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -  returns the difference of a and b
 * @a: first int
 * @b: second int
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of a and b
 * @a: first int
 * @b: second int
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - devides two integrs
 * @a: first int
 * @b: second int
 * Return: the quotien
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two integrs
 * @a: first int
 * @b: second int
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
